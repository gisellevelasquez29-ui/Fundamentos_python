#operadores de comparacion

print(1==2) #si es igual a 1, devuelve True
print(1==2) #si es igual a 2, devuelve False

print(2!=2) #si es diferente a 1, devuelve True
print(2!=2) #si es diferente a 2, devuelve False

print(3 > 7) #si es mayor que 7, devuelve True
print(3 < 2) #si es menor que 2, devuelve False
print(1>=3) #si es mayor o igual a 3, devuelve False
print(3<=1) #si es menor o igual a 1, devuelve True

