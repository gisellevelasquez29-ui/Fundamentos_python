#operadores de asignacion

x = 5
print(x)

x = x + 2 #suma 2 a x y asigna el resultado a x
print(x)
x += 2 #operador de asignación de suma, equivalente a x = x + 2
print(x)

x = x - 3 #resta
x -= 3
print(x)

x = x * 4 #multiplicacion
x *= 4
print(x)


x = x / 2 #division
x /= 2
print(x)


x = x % 3 #modulo
x %= 3
print(x)


x = x ** 2 #potencia
x **= 2
print(x)


x = 10
print(x)

#walrus operator (operador morsa) asignación de expresión
print(x := 10) #asigna 10 a x y luego imprime el valor de x



